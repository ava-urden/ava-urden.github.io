function applyLang(lang) {
  const d = dict[lang] || dict.sv;
  document.documentElement.setAttribute("lang", lang);

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (d[key] !== undefined) el.textContent = d[key];
  });

  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.getAttribute("data-i18n-html");
    if (d[key] !== undefined) el.innerHTML = d[key];
  });

  document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    const key = el.getAttribute("data-i18n-aria");
    if (d[key] !== undefined) el.setAttribute("aria-label", d[key]);
  });

  document.querySelectorAll("[data-cv-link]").forEach((link) => {
    link.setAttribute(
      "href",
      lang === "en" ? "./cv/ava-urden-cv-en.pdf" : "./cv/ava-urden-cv-sv.pdf"
    );
    link.setAttribute("download", "");
  });

  document.querySelectorAll(".lang__btn").forEach((btn) => {
    if (btn.dataset.lang === lang) {
      btn.classList.add("is-active");
    } else {
      btn.classList.remove("is-active");
    }
  });

  localStorage.setItem("lang", lang);
  setDataLayerState({ language: lang });
}

function initFilters() {
  const buttons = document.querySelectorAll(".filter-btn");
  const cards = document.querySelectorAll(".case");

  if (!buttons.length) return;

  buttons.forEach((btn) => {
    btn.addEventListener("click", () => {
      buttons.forEach((b) => b.classList.remove("is-active"));
      btn.classList.add("is-active");

      const filterValue = btn.getAttribute("data-filter");

      cards.forEach((card) => {
        const tags = (card.getAttribute("data-tags") || "")
          .split(",")
          .map((t) => t.trim());

        if (filterValue === "all" || tags.includes(filterValue)) {
          card.style.display = "flex";
        } else {
          card.style.display = "none";
        }
      });
    });
  });
}

function initReveal() {
  const items = document.querySelectorAll(".reveal");
  if (!items.length) return;

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.2 }
  );

  items.forEach((el) => observer.observe(el));
}

function initHeader() {
  const header = document.querySelector(".header");
  if (!header) return;

  const update = (y) => {
    if (y > 10) header.classList.add("header--scrolled");
    else header.classList.remove("header--scrolled");
  };

  if (window.lenis) {
    window.lenis.on("scroll", ({ scroll }) => update(scroll));
    update(window.lenis.scroll || 0);
  } else {
    const onScroll = () => update(window.scrollY);
    window.addEventListener("scroll", onScroll, { passive: true });
    onScroll();
  }
}

function initSectionObserver() {
  const sections = document.querySelectorAll(".section-block");
  if (!sections.length) return;

  const navLinks = Array.from(document.querySelectorAll(".nav a[href^='#']"));
  const linkMap = new Map();
  const seenSections = new Set();
  navLinks.forEach((link) => {
    const id = link.getAttribute("href").slice(1);
    if (id) linkMap.set(id, link);
  });

  const getSectionTitle = (section) => {
    const heading = section.querySelector("h1, h2, h3");
    return heading ? heading.textContent.trim() : "";
  };

  const setActive = (section) => {
    sections.forEach((s) => s.classList.remove("is-active"));
    section.classList.add("is-active");

    navLinks.forEach((link) => link.classList.remove("is-active"));
    const id = section.getAttribute("id");
    if (id && linkMap.has(id)) linkMap.get(id).classList.add("is-active");
  };

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) return;
        setActive(entry.target);

        const id = entry.target.getAttribute("id");
        if (id && !seenSections.has(id)) {
          seenSections.add(id);
          const sectionTitle = getSectionTitle(entry.target);
          pushDataLayerEvent("view_section", {
            ...getBaseEventData(),
            section_name: id,
            section_title: sectionTitle || undefined
          });
        }
      });
    },
    { threshold: 0.35 }
  );

  sections.forEach((section) => observer.observe(section));
}

function getHeaderOffset() {
  const header = document.querySelector(".header");
  return header ? header.offsetHeight : 0;
}

function initLenis() {
  if (typeof Lenis === "undefined") return;
  if (window.matchMedia && window.matchMedia("(prefers-reduced-motion: reduce)").matches) return;

  const lenis = new Lenis({
    lerp: 0.18,
    wheelMultiplier: 1,
    infinite: false,
    gestureOrientation: "vertical",
    normalizeWheel: false,
    smoothTouch: false
  });

  window.lenis = lenis;

  function raf(time) {
    lenis.raf(time);
    requestAnimationFrame(raf);
  }
  requestAnimationFrame(raf);

  document.querySelectorAll("a[href^='#']").forEach((link) => {
    link.addEventListener("click", (event) => {
      const href = link.getAttribute("href");
      if (!href || href === "#" || href === "#top") return;
      const target = document.querySelector(href);
      if (!target) return;
      event.preventDefault();
      lenis.scrollTo(target, { offset: -getHeaderOffset() });
    });
  });
}

